"""Build a static, browser-viewable visualisation of `docs/provenance.ttl`.

Loads the PROV-O Turtle graph, projects it to Cytoscape.js elements,
and writes:

    docs/site/index.html   - viewer (Cytoscape.js via CDN; offline-capable
                             once the CDN bundle is cached by the browser)
    docs/site/graph.json   - the elements (nodes + edges) the viewer reads
    docs/site/style.css    - minimal layout + filter-chip styles
    docs/site/README.md    - rule-14 / rule-13 note

The viewer supports:
    - class filter (Claim / Source / Activity / Agent / Figure / Build / Commit)
    - verification-rung filter (lit-read / ai-confirmed / lit-retrieved / ...)
    - click-to-inspect (full triple list for the clicked entity)
    - search by label

Rule 14: deployment of THIS site (the provenance-graph viewer) to GitHub
Pages was explicitly consented to by the human author (Florian Krebs)
on 2026-05-04 — see docs/logbook.md (Q14 entry) for the consent record
and .github/workflows/pages.yml for the deploy workflow. Consent does
not extend to other rule-14 gates: public-mirror push of the full
repository, Zenodo deposit, and arXiv submission each require their
own explicit consent.

Rule 13: redacted markers in the graph are passed through verbatim;
no live values are looked up. The script also refuses to write the site
if any node label or comment matches a known live-credential pattern.
"""

