def main() -> int:
    if not TTL_IN.exists():
        print(f"error: {TTL_IN} does not exist", file=sys.stderr)
        return 1
    g = Graph()
    g.parse(TTL_IN, format="turtle")
    nodes = collect_nodes(g)
    edges = collect_edges(g, nodes)
    check_rule_13(nodes)
    elements = write_graph_json(nodes, edges)
    import subprocess
    sha = subprocess.run(["git", "rev-parse", "--short", "HEAD"],
                         capture_output=True, text=True, cwd=REPO_ROOT).stdout.strip() or "(no git)"
    write_html(elements, sha)
    (SITE_DIR / "style.css").write_text(CSS)
    (SITE_DIR / "README.md").write_text(SITE_README)
    print(f"OK: wrote {SITE_DIR}/index.html "
          f"({len(elements['nodes'])} nodes, {len(elements['edges'])} edges)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
