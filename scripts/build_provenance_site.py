# Rule-13 sentinel patterns. The graph must NEVER carry a live credential
# in any literal. These are conservative regexps; matching means refuse.
LIVE_CREDENTIAL_PATTERNS = [
    re.compile(r"AKIA[0-9A-Z]{16}"),               # AWS access key
    re.compile(r"-----BEGIN (RSA |EC |OPENSSH )?PRIVATE KEY-----"),
    re.compile(r"eyJ[A-Za-z0-9_-]{20,}\.[A-Za-z0-9_-]{20,}\.[A-Za-z0-9_-]{20,}"),  # JWT
    re.compile(r"xox[baprs]-[A-Za-z0-9-]{10,}"),   # Slack tokens
    re.compile(r"ghp_[A-Za-z0-9]{30,}"),           # GitHub PAT
]


def short(uri: str) -> str:
    if uri.startswith(str(OID)):
        return "oid:" + uri[len(str(OID)):]
    if uri.startswith(str(PROV)):
        return "prov:" + uri[len(str(PROV)):]
    if uri.startswith(str(DCTERMS)):
        return "dct:" + uri[len(str(DCTERMS)):]
    if uri.startswith(str(RDFS)):
        return "rdfs:" + uri[len(str(RDFS)):]
    if uri.startswith(str(FOAF)):
        return "foaf:" + uri[len(str(FOAF)):]
    return uri


def class_label_for(types: set[URIRef]) -> str:
    candidates = [short(str(t)).removeprefix("oid:").removeprefix("prov:")
                  for t in types]
    # Prefer the most specific local class.
    priority = ["Claim", "Source", "Transcript", "Figure", "Commit",
                "VerificationStatus", "Build", "PipelineStage",
                "AIAgent", "HumanResearcher", "Agent", "Activity", "Entity"]
    for p in priority:
        if p in candidates:
            return p
    return candidates[0] if candidates else "Entity"


def collect_nodes(g: Graph) -> dict[str, dict]:
    nodes: dict[str, dict] = {}
    for s in set(g.subjects()):
        if not isinstance(s, URIRef):
            continue
        types = set(g.objects(s, RDF.type))
        if not types and not any(g.predicate_objects(s)):
            continue
        cls = class_label_for(types) if types else "Entity"
        title = (g.value(s, DCTERMS.title)
                 or g.value(s, FOAF.name)
                 or g.value(s, RDFS.label)
                 or g.value(s, DCTERMS.identifier))
        comment = g.value(s, RDFS.comment) or g.value(s, DCTERMS.description)
        verification = g.value(s, OID.hasVerificationStatus)
        triples = []
        for p, o in g.predicate_objects(s):
            triples.append({"p": short(str(p)), "o": str(o),
                            "is_uri": isinstance(o, URIRef)})
        nodes[str(s)] = {
            "id": str(s),
            "short": short(str(s)),
            "class": cls,
            "title": str(title) if title else short(str(s)),
            "comment": str(comment) if comment else "",
            "verification": short(str(verification)) if verification else "",
            "types": [short(str(t)) for t in types],
            "triples": triples,
        }
    return nodes


def collect_edges(g: Graph, nodes: dict[str, dict]) -> list[dict]:
    edges: list[dict] = []
    edge_predicates = {
        PROV.wasDerivedFrom, PROV.wasGeneratedBy, PROV.wasAttributedTo,
        PROV.used, PROV.wasAssociatedWith, PROV.wasInformedBy,
        PROV.generated, OID.hasVerificationStatus,
    }
    for s, p, o in g:
        if p not in edge_predicates:
            continue
        if not isinstance(s, URIRef) or not isinstance(o, URIRef):
            continue
        if str(s) not in nodes or str(o) not in nodes:
            continue
        pred_local = short(str(p)).split(":", 1)[-1]
        edges.append({
            "id": f"{s}--{pred_local}--{o}",
            "source": str(s),
            "target": str(o),
            "predicate": pred_local,
        })
    return edges


def check_rule_13(nodes: dict[str, dict]) -> None:
    leaks: list[tuple[str, str]] = []
    for nid, node in nodes.items():
        haystacks = [node["title"], node["comment"]]
        haystacks.extend(t["o"] for t in node["triples"])
        for hay in haystacks:
            for pattern in LIVE_CREDENTIAL_PATTERNS:
                if pattern.search(hay):
                    leaks.append((nid, pattern.pattern))
    if leaks:
        print("RULE-13 GUARD: refusing to write site; live-credential pattern detected.",
              file=sys.stderr)
        for nid, pat in leaks:
            print(f"  {nid}: {pat}", file=sys.stderr)
        sys.exit(2)


def write_graph_json(nodes: dict[str, dict], edges: list[dict]) -> dict:
    elements = {
        "nodes": [
            {"data": {
                "id": n["id"],
                "short": n["short"],
                "label": n["title"],
                "cls": n["class"],
                "comment": n["comment"],
                "verification": n["verification"],
                "types": n["types"],
                "triples": n["triples"],
            }} for n in nodes.values()
        ],
        "edges": [
            {"data": {
                "id": e["id"],
                "source": e["source"],
                "target": e["target"],
                "label": EDGE_PALETTE.get(e["predicate"], {}).get("label", e["predicate"]),
                "predicate": e["predicate"],
            }} for e in edges
        ],
    }
    SITE_DIR.mkdir(parents=True, exist_ok=True)
    out = SITE_DIR / "graph.json"
    out.write_text(json.dumps(elements, indent=2, ensure_ascii=False))
    return elements


